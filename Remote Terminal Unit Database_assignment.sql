-- 1. RTU Table (Parent Table)
create database RTUdb;
use rtudb;
CREATE TABLE RTU (
    RTU_ID INT PRIMARY KEY,
    LOCATION VARCHAR(100),
    IP_ADDRESS VARCHAR(15)
);

-- 2. SENSORS Table
-- Linked to RTU via RTU_ID
CREATE TABLE SENSORS (
    SENSOR_ID INT PRIMARY KEY,
    RTU_ID INT,
    STATUS VARCHAR(20), -- e.g., 'ENABLED', 'DISABLED'
    SENSOR_TYPE VARCHAR(50),
    REQ_VAL INT,
    CURR_VAL INT,
    FOREIGN KEY (RTU_ID) REFERENCES RTU(RTU_ID)
);

-- 3. USERS Table
CREATE TABLE USERS (
    USER_ID INT PRIMARY KEY,
    USER_NAME VARCHAR(100),
    ROLE VARCHAR(50) -- e.g., 'MONITOR', 'ADMINISTER'
);

-- 4. CONTROLLER Table (Junction Table)
-- Maps which User manages which Sensor
CREATE TABLE CONTROLLER (
    USER_ID INT,
    SENSOR_ID INT,
    PRIMARY KEY (USER_ID, SENSOR_ID),
    FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID),
    FOREIGN KEY (SENSOR_ID) REFERENCES SENSORS(SENSOR_ID)
);
